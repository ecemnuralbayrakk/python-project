import turtle


def draw_square():
    window = turtle.Screen()


    brad = turtle.Turtle()
    brad.shape("turtle")
    brad.color("blue")
    
    brad.forward(100)
    brad.right(120)
    brad.forward(100)
    brad.right(120)
    brad.forward(100)
    brad.right(120)
    
    
    
    brad.forward(100)
    brad.right(90)
    brad.forward(100)
    brad.right(90)
    brad.forward(100)
    brad.right(90)
    brad.forward(100)
    brad.right(90)
    
    brad.forward(100)
    brad.right(60)
    brad.forward(100)
    brad.right(60)
    brad.forward(100)
    brad.right(60)
    brad.forward(100)
    brad.right(60)
    brad.forward(100)
    brad.right(60)
    brad.forward(100)
    brad.right(60)
    

    window.exitonclick()


draw_square()
